CREATE PROCEDURE [dbo].[счф_о] (@дог_код INT)
AS
  SET NOCOUNT ON;

  SELECT
    счф_код,
    счф_док,
    счф_дата,
    счф_сум,
    счф_ном,
    счф_сут,
    опл_дата,
    опл_док,
    счф_цена,
    счф_инф,
    дог_код,
    дата_с,
    дата_по,
    счф_ски,
    счф_итого,
    dbo.Фс_счф_исп_имя(счф_код)AS счет_исп_имя,
  dbo.Фс_счф_исп_код(счф_код)AS счет_исп_код
    
  FROM
    счф
  WHERE  ( дог_код = @дог_код )
GO