CREATE PROCEDURE [dbo].[орг_о]
(
	@орг_код int
)
AS
	SET NOCOUNT ON;
SELECT     орг_код, орг_имя, орг_имя_кор, орг_имя_пол, преамбула, счет_номер, счет_банк, счет_мфо, унп, адрес, город, стр_код, рук_фио_кор, 
                      рук_фио_пол, рук_должность, дейст_на_осн, тел_рук, контакт_фио, тел_контакт, тел_гор, тел_факс, тел_Velcom, тел_MTC, тел_Life, 
                      е_маил
FROM         орг
WHERE     (орг_код = @орг_код)
GO