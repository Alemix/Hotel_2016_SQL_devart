CREATE PROCEDURE [dbo].[бар_дви_о]
AS
	SET NOCOUNT ON;
SELECT
   бар_дви.дви_код,
  бар_дви.тов_код,
  бар_дви.дата,
  бар_дви.кол,
  бар_дви.рас,
  бар_дви.при,
  бар_тов.тов_вид,
  бар_тов.тов_имя,
  бар_тов.тов,
  бар_дви.опер_код,
  бар_дви.цена,
  бар_дви.сумма,
 dbo.Фс_опер_имя(бар_дви.опер_код) as опер_имя
FROM
  бар_дви
INNER JOIN бар_тов
  ON бар_дви.тов_код = бар_тов.тов_код
GO